            Keraton Yogyakarta, atau Karaton Ngayogyakarta Hadiningrat, adalah istana resmi Kesultanan Yogyakarta yang didirikan oleh Sri Sultan Hamengkubuwono I pada tahun 1755 setelah Perjanjian Giyanti. Berfungsi sebagai pusat pemerintahan, budaya, dan spiritual, keraton ini dirancang dengan filosofi kosmologi Jawa, mencerminkan harmoni antara manusia, alam, dan Tuhan. Lokasinya dipilih secara simbolis di antara Gunung Merapi dan Pantai Selatan, dengan Alun-Alun Utara dan Selatan sebagai pusat aktivitas. Arsitekturnya khas Jawa, menampilkan gerbang megah seperti Regol Brojonolo, bangunan inti seperti Bangsal Kencono (balairung utama), dan paviliun seperti Siti Hinggil dan Kedaton. Keraton juga menyimpan berbagai pusaka, seni, dan tradisi seperti gamelan, tarian, serta upacara adat seperti Sekaten dan Grebeg.
