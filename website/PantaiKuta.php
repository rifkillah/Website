                Candi Borobudur adalah mahakarya Buddha terbesar di dunia, dibangun sekitar abad ke-9 pada masa pemerintahan Wangsa Syailendra. Terletak di Magelang, Jawa Tengah, candi megah ini berbentuk piramida berundak dengan enam teras persegi dan tiga pelingkar lingkaran, dimahkotai stupa utama setinggi 35 meter. Dindingnya dipenuhi 2.672 panel relief yang menggambarkan kisah kehidupan Buddha dan ajaran Dharma, sementara 504 arca Buddha duduk bersila dalam stupa berlubang. Dibangun dari balok vulkanik tanpa perekat, Borobudur sempat terkubur abu vulkanik sebelum ditemukan kembali pada 1814 oleh Sir Thomas Stamford Raffles. Kini sebagai Situs Warisan Dunia UNESCO, candi ini menjadi pusat ziarah umat Buddha sekaligus ikon wisata Indonesia yang terkenal dengan pemandangan sunrise-nya yang memukau.
